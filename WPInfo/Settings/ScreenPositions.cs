
namespace Ventajou.WPInfo
{
    /// <summary>
    /// Defines the position of the information text on the screen
    /// </summary>
    public enum ScreenPositions
    {
        /// <summary>
        /// The information text will be rendered in the upper left corner of the screen.
        /// </summary>
        TopLeft,
        /// <summary>
        /// The information text will be rendered in the upper right corner of the screen.
        /// </summary>
        TopRight,
        /// <summary>
        /// The information text will be rendered in the lower left corner of the screen.
        /// </summary>
        BottomLeft,
        /// <summary>
        /// The information text will be rendered in the lower right corner of the screen.
        /// </summary>
        BottomRight
    }
}
